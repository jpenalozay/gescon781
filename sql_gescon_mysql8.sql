insert into fecha (ID, DIA, MES, ANIO, FECHA, DIA_NOMBRE, DIA_NOMBRE_CORTO, FERIADO, LABORAL, FIN_SEMANA)
select DENSE_RANK() OVER(ORDER BY date(FECHA) ASC) as id,Date_format(a.Date,'%d') as dia,Date_format(a.Date,'%m') as mes,YEAR(a.Date) AS anio, a.Date AS fecha, 
CASE (Date_format(a.Date,'%W'))    
    WHEN 'Monday' THEN 'Lunes'
    WHEN 'Tuesday' THEN 'Martes'
    WHEN 'Wednesday' THEN 'Miércoles'
    WHEN 'Thursday' THEN 'Jueves'
    WHEN 'Friday' THEN 'Viernes'
    WHEN 'Saturday' THEN 'Sábado'
    WHEN 'Sunday' THEN 'Domingo'
    END dia_nombre,
CASE (Date_format(a.Date,'%W'))    
    WHEN 'Monday' THEN 'Lu'
    WHEN 'Tuesday' THEN 'Ma'
    WHEN 'Wednesday' THEN 'Mi'
    WHEN 'Thursday' THEN 'Ju'
    WHEN 'Friday' THEN 'Vi'
    WHEN 'Saturday' THEN 'Sa'
    WHEN 'Sunday' THEN 'Do'
    END dia_nombre_corto,
'NO' as feriado,
CASE (Date_format(a.Date,'%W'))    
    WHEN 'Monday' THEN 'SI'
    WHEN 'Tuesday' THEN 'SI'
    WHEN 'Wednesday' THEN 'SI'
    WHEN 'Thursday' THEN 'SI'
    WHEN 'Friday' THEN 'SI'
    WHEN 'Saturday' THEN 'NO'
    WHEN 'Sunday' THEN 'NO'
    END dia_laboral,
CASE (Date_format(a.Date,'%W'))    
    WHEN 'Monday' THEN 'NO'
    WHEN 'Tuesday' THEN 'NO'
    WHEN 'Wednesday' THEN 'NO'
    WHEN 'Thursday' THEN 'NO'
    WHEN 'Friday' THEN 'NO'
    WHEN 'Saturday' THEN 'SI'
    WHEN 'Sunday' THEN 'SI'
    END fin_de_semana
from (
WITH RECURSIVE date_ranges AS (SELECT '2022-01-01' dt UNION ALL SELECT dt + INTERVAL 1 DAY FROM date_ranges WHERE dt + INTERVAL 1 DAY <= '2023-12-31')
SELECT dt as Date FROM date_ranges
) a;

